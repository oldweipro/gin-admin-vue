<template>
  <div>
    <div class="gva-form-box">
      <el-form :model="formData" ref="elFormRef" label-position="right" :rules="rule" label-width="80px">
        <el-form-item label="反馈的文字内容:" prop="feedback_text">
          <el-input v-model="formData.feedback_text" :clearable="false" placeholder="请输入" />
        </el-form-item>
        <el-form-item label="反馈用户的ID:" prop="user_id">
          <el-input v-model.number="formData.user_id" :clearable="false" placeholder="请输入" />
        </el-form-item>
        <el-form-item label="反馈的上级ID:" prop="parent_id">
          <el-input v-model.number="formData.parent_id" :clearable="false" placeholder="请输入" />
        </el-form-item>
        <el-form-item>
          <el-button type="primary" @click="save">保存</el-button>
          <el-button type="primary" @click="back">返回</el-button>
        </el-form-item>
      </el-form>
    </div>
  </div>
</template>

<script>
export default {
  name: 'Feedback'
}
</script>

<script setup>
import {
  createFeedback,
  updateFeedback,
  findFeedback
} from '@/api/feedback'

// 自动获取字典
import { getDictFunc } from '@/utils/format'
import { useRoute, useRouter } from "vue-router"
import { ElMessage } from 'element-plus'
import { ref, reactive } from 'vue'
const route = useRoute()
const router = useRouter()

const type = ref('')
const formData = ref({
            feedback_text: '',
            user_id: 0,
            parent_id: 0,
        })
// 验证规则
const rule = reactive({
               feedback_text : [{
                   required: true,
                   message: '请输入反馈内容',
                   trigger: ['input','blur'],
               }],
               user_id : [{
                   required: true,
                   message: '',
                   trigger: ['input','blur'],
               }],
               parent_id : [{
                   required: true,
                   message: '',
                   trigger: ['input','blur'],
               }],
})

const elFormRef = ref()

// 初始化方法
const init = async () => {
 // 建议通过url传参获取目标数据ID 调用 find方法进行查询数据操作 从而决定本页面是create还是update 以下为id作为url参数示例
    if (route.query.id) {
      const res = await findFeedback({ id: route.query.id })
      if (res.code === 0) {
        formData.value = res.data.refeedback
        type.value = 'update'
      }
    } else {
      type.value = 'create'
    }
}

init()
// 保存按钮
const save = async() => {
      elFormRef.value?.validate( async (valid) => {
         if (!valid) return
            let res
           switch (type.value) {
             case 'create':
               res = await createFeedback(formData.value)
               break
             case 'update':
               res = await updateFeedback(formData.value)
               break
             default:
               res = await createFeedback(formData.value)
               break
           }
           if (res.code === 0) {
             ElMessage({
               type: 'success',
               message: '创建/更改成功'
             })
           }
       })
}

// 返回按钮
const back = () => {
    router.go(-1)
}

</script>

